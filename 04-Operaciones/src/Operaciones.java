public class Operaciones {
	
	/**
	 * 
	 * @param num1= Recibe un numero entero
	 * @param num2= Recibe un numero entero
	 * @return = Regresa la suma de los numeros
	 */

	public int sumar(int num1, int num2){
		return num1 + num2;
		
	}
	/**
	 * 
	 * @param num1 = Recibe un numero entero
	 * @param num2 = Recibe un numero entero
	 * @return = Regresa la resta de los numero
	 */
	public int restar(int num1, int num2){
		return num1 - num2;
	}
	/**
	 * 
	 * @param num1 = Recibe un numero entero
	 * @param num2 = Recibe un numero entero
	 * @return = Regresa la multiplicacion de los numeros
	 */
	public int multiplica (int num1, int num2){
		return num1 * num2;
	}
	/**
	 * 
	 * @param num1 = Recibe un numero entero
	 * @param num2 = Recibe un numero entero
	 * @return = Regresa la división de los numeros
	 */
	public int divide (int num1, int  num2){
		return num1/num2;
	}
}
