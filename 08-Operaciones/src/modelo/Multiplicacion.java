public class Multiplicacion implements Operaciones {
/**
 * @param n1 Recibe un numero entero
 * @param n2 Recibe un numero entero
 * @return Regresa la multiplicacion de dos muneros
 */
	public int operacion(int n1, int n2) {
		
		return n1 * n2;
	}

}
