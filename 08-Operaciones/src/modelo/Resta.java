public class Resta implements Operaciones {
/**
 * @param n1 Recibe un numero entero
 * @param n2 Recibe un numero entero
 * @return Regresa la resta de dos numeros
 */
	public int operacion(int n1, int n2) {
		
		return n1 - n2;
	}

}
